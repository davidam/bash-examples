NC='\033[0m' # No Color
BLACK='\033[0;30m'
RED='\033[0;31m'
GREEN='\033[0;32m'
ORANGE='\033[0;33m'
BLUE='\033[0;34m'
PURPLE='\033[0;35m'
CYAN='\033[0;36m'
GRAY='\033[0;37m'
DARKGRAY='\033[1;30m'

# Underline
UBlack='\033[4;30m'       # Black
URed='\033[4;31m'         # Red
UGreen='\033[4;32m'       # Green
UYellow='\033[4;33m'      # Yellow
UBlue='\033[4;34m'        # Blue
UPurple='\033[4;35m'      # Purple
UCyan='\033[4;36m'        # Cyan
UWhite='\033[4;37m'       # White

# Background
On_Black='\033[40m'       # Black
On_Red='\033[41m'         # Red
On_Green='\033[42m'       # Green
On_Yellow='\033[43m'      # Yellow
On_Blue='\033[44m'        # Blue
On_Purple='\033[45m'      # Purple
On_Cyan='\033[46m'        # Cyan
On_White='\033[47m'       # White

# High Intensity
IBlack='\033[0;90m'       # Black
IRed='\033[0;91m'         # Red
IGreen='\033[0;92m'       # Green
IYellow='\033[0;93m'      # Yellow
IBlue='\033[0;94m'        # Blue
IPurple='\033[0;95m'      # Purple
ICyan='\033[0;96m'        # Cyan
IWhite='\033[0;97m'       # White

# Bold High Intensity
BIBlack='\033[1;90m'      # Black
BIRed='\033[1;91m'        # Red
BIGreen='\033[1;92m'      # Green
BIYellow='\033[1;93m'     # Yellow
BIBlue='\033[1;94m'       # Blue
BIPurple='\033[1;95m'     # Purple
BICyan='\033[1;96m'       # Cyan
BIWhite='\033[1;97m'      # White

# High Intensity backgrounds
On_IBlack='\033[0;100m'   # Black
On_IRed='\033[0;101m'     # Red
On_IGreen='\033[0;102m'   # Green
On_IYellow='\033[0;103m'  # Yellow
On_IBlue='\033[0;104m'    # Blue
On_IPurple='\033[0;105m'  # Purple
On_ICyan='\033[0;106m'    # Cyan
On_IWhite='\033[0;107m'   # White


echo -e  "maindavid test is ${BLACK}black${NC}"
echo -e  "maindavid test is ${RED}red${NC}"
echo -e  "maindavid test is ${GREEN}green${NC}"
echo -e  "maindavid test is ${ORANGE}orange${NC}"
echo -e  "maindavid test is ${BLUE}blue${NC}"
echo -e  "maindavid test is ${PURPLE}purple${NC}"
echo -e  "maindavid test is ${CYAN}cyan${NC}"
echo -e  "maindavid test is ${GRAY}gray${NC}"
echo -e  "maindavid test is ${DARKGRAY}darkgray${NC}"


echo -e  "maindavid test is ${URed}red${NC}"
echo -e  "maindavid test is ${UGreen}green${NC}"
echo -e  "maindavid test is ${UBlue}blue${NC}"
echo -e  "maindavid test is ${UPurple}purple${NC}"
echo -e  "maindavid test is ${UCyan}cyan${NC}"



echo -e  "maindavid test is ${On_Black}black${NC}"
echo -e  "maindavid test is ${On_Red}red${NC}"
echo -e  "maindavid test is ${On_Green}green${NC}"
echo -e  "maindavid test is ${On_Blue}blue${NC}"
echo -e  "maindavid test is ${On_Purple}purple${NC}"
echo -e  "maindavid test is ${On_Cyan}cyan${NC}"


# Black        0;30     Dark Gray     1;30
# Red          0;31     Light Red     1;31
# Green        0;32     Light Green   1;32
# Brown/Orange 0;33     Yellow        1;33
# Blue         0;34     Light Blue    1;34
# Purple       0;35     Light Purple  1;35
# Cyan         0;36     Light Cyan    1;36
# Light Gray   0;37     White         1;37
